  // 2) Synonymes (ex: grey -> gray)
  export const COLOR_SYNONYMS = {
    grey: "gray",
    darkgrey: "darkgray",
    lightgrey: "lightgray",
    slategrey: "slategray",
    lightslategrey: "lightslategray",
  };
  
  // 3) Tout en lowercase
  export const COLOR_TRANSLATIONS_LOWER = Object.fromEntries(
    Object.entries(COLOR_TRANSLATIONS).map(([k, v]) => [k.toLowerCase(), v])
  );
  
  // 4) Options autocomplete
  export const COLOR_OPTIONS = Object.entries(COLOR_TRANSLATIONS).map(
    ([value, label]) => ({ value, label })
  );
  
  // 5) Normalisation
  export function normalizeColorName(name = "") {
    return COLOR_SYNONYMS[name.trim().toLowerCase()] || name.trim().toLowerCase();
  }
  
  // 6) Récupération du label arabe
  export function getArabicColorLabel(name) {
    if (!name) return "";
    if (/[ء-ي]/.test(name)) return name; // déjà arabe
  
    const normalized = normalizeColorName(String(name));
    if (COLOR_TRANSLATIONS_LOWER[normalized])
      return COLOR_TRANSLATIONS_LOWER[normalized];
  
    const alt = normalized.replace(/[_\-]+/g, "");
    if (COLOR_TRANSLATIONS_LOWER[alt]) return COLOR_TRANSLATIONS_LOWER[alt];
  
    return name;
  }
  